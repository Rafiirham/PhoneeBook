    private void Save()
    {
        Connection conn = null;
        PreparedStatement pstmt = null;       
                
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/phonebook","root","");
            pstmt = conn.prepareStatement("insert into phonebook (ID,Name,PhoneNumber,Email) values (?,?,?,?)");
            pstmt.setString(1, txtID.getText());
            pstmt.setString(2, txtName.getText());
            pstmt.setString(3, txtPhone.getText()); 
            pstmt.setString(4, txtEmail.getText());
            
            int i = pstmt.executeUpdate();
            
            if (i > 0)
            {
                JOptionPane.showMessageDialog(null, "[" + txtName.getText() + "] has been saved successfully.");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "There is something wrong with the ongoing process.");
            }
            pstmt.close();
            conn.close();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        LoadDataTable();
    }
    
