    private void Search()
    {
         Connection conn = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;
        DefaultTableModel TabModel;
        
        String ColName;
        String Option = (String)cboOption.getSelectedItem();
        
        if (Option == "Name")
        {
            ColName = "Name";
        }
        else if (Option == "Phone Number")
        {
            ColName = "PhoneNumber";
        }
        else
        {
            ColName = "Email";
        }
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/phonebook","root","");
            Object[] column = {"ID","Name","Phone Number","E-Mail"};
            TabModel=new DefaultTableModel(null, column);
            tblData.setModel(TabModel);
            TabModel.getDataVector().removeAllElements();
            pstmt = conn.prepareStatement("SELECT * FROM Phonebook WHERE " + ColName + " LIKE '%" + txtSearch.getText() + "%'");
            rs = pstmt.executeQuery();
            while(rs.next())
            {
                Object[] data={rs.getString("ID"),
                               rs.getString("Name"),
                               rs.getString("PhoneNumber"),
                               rs.getString("Email")                               
                              };
                TabModel.addRow(data);
            }
            pstmt.close();
            rs.close();
            conn.close();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
